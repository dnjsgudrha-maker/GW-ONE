export function generateWorkText(jobType, selected = []) {
  const template = WORK_TEMPLATES[jobType];
  if (!template) return "";

  return selected
    .map((item) => template.sentences[item])
    .filter(Boolean)
    .join(" ");
}

export function generateResultText(jobType, selected = []) {
  const template = WORK_TEMPLATES[jobType];
  if (!template) return "";

  const results = selected
    .map((item) => template.resultMap[item])
    .filter(Boolean);

  return [...new Set(results)].join(", ");
}
